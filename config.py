def get_t_max(map_name):
    """Return the evaluation/training horizon for a FrozenLake map name."""
    if map_name == "4x4":
        return 100
    if map_name == "8x8":
        return 200
    try:
        n = int(str(map_name).split("x")[0])
        return 4 * n * n
    except (ValueError, IndexError, TypeError):
        return 200


T_MAX = get_t_max(MAP_NAME)

